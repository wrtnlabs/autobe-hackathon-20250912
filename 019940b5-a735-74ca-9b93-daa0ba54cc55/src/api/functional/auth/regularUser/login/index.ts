import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEventRegistrationRegularUser } from "../../../../structures/IEventRegistrationRegularUser";

/**
 * Authenticate regular user (event_registration_regular_users) and issue access
 * tokens.
 *
 * Authenticates a regular user using their email and password and issues JWT
 * tokens if credentials are valid and email is verified in the
 * 'event_registration_regular_users' table.
 *
 * @param props.connection
 * @param props.body Request body containing login credentials of the regular
 *   user.
 * @setHeader token.access Authorization
 *
 * @path /auth/regularUser/login
 * @accessor api.functional.auth.regularUser.login.loginRegularUser
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function loginRegularUser(
  connection: IConnection,
  props: loginRegularUser.Props,
): Promise<loginRegularUser.Response> {
  const output: loginRegularUser.Response =
    true === connection.simulate
      ? loginRegularUser.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...loginRegularUser.METADATA,
            path: loginRegularUser.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace loginRegularUser {
  export type Props = {
    /** Request body containing login credentials of the regular user. */
    body: IEventRegistrationRegularUser.ILogin;
  };
  export type Body = IEventRegistrationRegularUser.ILogin;
  export type Response = IEventRegistrationRegularUser.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/regularUser/login",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/regularUser/login";
  export const random = (): IEventRegistrationRegularUser.IAuthorized =>
    typia.random<IEventRegistrationRegularUser.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: loginRegularUser.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: loginRegularUser.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

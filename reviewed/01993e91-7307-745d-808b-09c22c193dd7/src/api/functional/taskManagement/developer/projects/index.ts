  export const METADATA = {
    method: "PATCH",
    path: "/taskManagement/developer/projects",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/taskManagement/developer/projects";

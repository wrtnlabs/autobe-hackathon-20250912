import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IJobPerformanceEvalEmployee } from "../../../../structures/IJobPerformanceEvalEmployee";

/**
 * Employee login with email and password, issuing JWT tokens, related to
 * job_performance_eval_employees table.
 *
 * Authenticate employee user via email and password, issuing JWT tokens upon
 * success. Uses job_performance_eval_employees table for credential validation
 * and user identity. This is a public endpoint enabling regular users to
 * login.
 *
 * Ensures password hash comparison and secure token issuance with
 * IJobPerformanceEvalEmployee.IAuthorized in response.
 *
 * Critical for member-role authentication workflows, paired with join and
 * refresh operations.
 *
 * Authentication failures result in HTTP 401 with clear error messages.
 *
 * Allows session management consistent with JWT-based security.
 *
 * @param props.connection
 * @param props.body Login credentials payload for employee authentication.
 * @setHeader token.access Authorization
 *
 * @path /auth/employee/login
 * @accessor api.functional.auth.employee.login.loginEmployee
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function loginEmployee(
  connection: IConnection,
  props: loginEmployee.Props,
): Promise<loginEmployee.Response> {
  const output: loginEmployee.Response =
    true === connection.simulate
      ? loginEmployee.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...loginEmployee.METADATA,
            path: loginEmployee.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace loginEmployee {
  export type Props = {
    /** Login credentials payload for employee authentication. */
    body: IJobPerformanceEvalEmployee.ILogin;
  };
  export type Body = IJobPerformanceEvalEmployee.ILogin;
  export type Response = IJobPerformanceEvalEmployee.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/employee/login",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/employee/login";
  export const random = (): IJobPerformanceEvalEmployee.IAuthorized =>
    typia.random<IJobPerformanceEvalEmployee.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: loginEmployee.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: loginEmployee.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

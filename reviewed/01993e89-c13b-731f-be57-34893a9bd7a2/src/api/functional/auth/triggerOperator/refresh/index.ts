import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { INotificationWorkflowTriggerOperator } from "../../../../structures/INotificationWorkflowTriggerOperator";

/**
 * Refresh JWT tokens for trigger operator user
 * (notification_workflow_triggeroperators).
 *
 * Renew JWT access tokens for an authenticated trigger operator using a refresh
 * token. Validates token validity and issues new access and refresh tokens in
 * INotificationWorkflowTriggerOperator.IAuthorized response format.
 *
 * Supports seamless authentication lifecycle and enhances security by enforcing
 * token expiry and rotation.
 *
 * Operates on Prisma schema table notification_workflow_triggeroperators with
 * fields critical to authentication state.
 *
 * Accessible publicly but requires a valid refresh token for token renewal.
 *
 * Complements join and login operations for triggerOperator authentication
 * workflow.
 *
 * Controller applies proper security guards to ensure refresh token validation
 * prior to execution.
 *
 * Following RESTful and security best practices with role-specific
 * authorization flows.
 *
 * @param props.connection
 * @param props.body Refresh token input for renewing JWT tokens.
 * @setHeader token.access Authorization
 *
 * @path /auth/triggerOperator/refresh
 * @accessor api.functional.auth.triggerOperator.refresh.refreshTriggerOperator
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshTriggerOperator(
  connection: IConnection,
  props: refreshTriggerOperator.Props,
): Promise<refreshTriggerOperator.Response> {
  const output: refreshTriggerOperator.Response =
    true === connection.simulate
      ? refreshTriggerOperator.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshTriggerOperator.METADATA,
            path: refreshTriggerOperator.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshTriggerOperator {
  export type Props = {
    /** Refresh token input for renewing JWT tokens. */
    body: INotificationWorkflowTriggerOperator.IRefresh;
  };
  export type Body = INotificationWorkflowTriggerOperator.IRefresh;
  export type Response = INotificationWorkflowTriggerOperator.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/triggerOperator/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/triggerOperator/refresh";
  export const random = (): INotificationWorkflowTriggerOperator.IAuthorized =>
    typia.random<INotificationWorkflowTriggerOperator.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshTriggerOperator.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshTriggerOperator.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

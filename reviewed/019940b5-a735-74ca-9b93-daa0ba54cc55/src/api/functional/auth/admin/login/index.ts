import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEventRegistrationAdmin } from "../../../../structures/IEventRegistrationAdmin";

/**
 * Authenticate as admin and receive JWT token.
 *
 * Authenticate an admin user with their email and password hash. Verifies
 * credentials against the 'event_registration_admins' table and returns a JWT
 * authorization token on success. Ensures email verification status is true
 * before authentication. Restricted to admin role users.
 *
 * @param props.connection
 * @param props.body Admin login request payload containing email and password
 *   hash
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/login
 * @accessor api.functional.auth.admin.login.loginAdminUser
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function loginAdminUser(
  connection: IConnection,
  props: loginAdminUser.Props,
): Promise<loginAdminUser.Response> {
  const output: loginAdminUser.Response =
    true === connection.simulate
      ? loginAdminUser.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...loginAdminUser.METADATA,
            path: loginAdminUser.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace loginAdminUser {
  export type Props = {
    /** Admin login request payload containing email and password hash */
    body: IEventRegistrationAdmin.ILogin;
  };
  export type Body = IEventRegistrationAdmin.ILogin;
  export type Response = IEventRegistrationAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/login",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/login";
  export const random = (): IEventRegistrationAdmin.IAuthorized =>
    typia.random<IEventRegistrationAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: loginAdminUser.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: loginAdminUser.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IJobPerformanceEvalEmployee } from "../../../../structures/IJobPerformanceEvalEmployee";

/**
 * Refresh JWT tokens for employee user, related to
 * job_performance_eval_employees table.
 *
 * Refresh JWT tokens using a valid refresh token for employee users. Validates
 * token association with the job_performance_eval_employees table.
 *
 * Returns new JWT tokens in the response for continued authenticated sessions.
 *
 * Facilitates seamless session continuity for member role users.
 *
 * Requires secure validation to prevent token misuse.
 *
 * Works as part of the full authentication flow along with join and login
 * operations.
 *
 * @param props.connection
 * @param props.body Refresh token payload for employee authentication renewal.
 * @setHeader token.access Authorization
 *
 * @path /auth/employee/refresh
 * @accessor api.functional.auth.employee.refresh.refreshEmployee
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshEmployee(
  connection: IConnection,
  props: refreshEmployee.Props,
): Promise<refreshEmployee.Response> {
  const output: refreshEmployee.Response =
    true === connection.simulate
      ? refreshEmployee.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshEmployee.METADATA,
            path: refreshEmployee.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshEmployee {
  export type Props = {
    /** Refresh token payload for employee authentication renewal. */
    body: IJobPerformanceEvalEmployee.IRefresh;
  };
  export type Body = IJobPerformanceEvalEmployee.IRefresh;
  export type Response = IJobPerformanceEvalEmployee.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/employee/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/employee/refresh";
  export const random = (): IJobPerformanceEvalEmployee.IAuthorized =>
    typia.random<IJobPerformanceEvalEmployee.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshEmployee.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshEmployee.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

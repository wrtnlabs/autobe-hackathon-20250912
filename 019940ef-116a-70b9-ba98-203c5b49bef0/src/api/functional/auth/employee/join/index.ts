/**
 * Register new employee user and issue JWT tokens, related to
 * job_performance_eval_employees table.
 *
 * This API endpoint allows new employee users to register for the Job
 * Performance Evaluation system. It captures essential authentication
 * credentials, including unique email and securely hashed password fields as
 * stored in the job_performance_eval_employees table. After registration, an
 * access token is issued represented by
 * IJobPerformanceEvalJobPerformanceEvalEmployee.IAuthorized type enabling
 * subsequent authenticated interactions.
 *
 * The operation validates the uniqueness of the email to prevent duplicate
 * accounts and ensures password hashing security. It enables member users to
 * create their accounts and gain authenticated access.
 *
 * It is publicly accessible without prior authentication but requires valid
 * user input conforming to defined schema.
 *
 * This operation is the first step in the authentication flow for employee role
 * users.
 *
 * Subsequent operations include login (to authenticate) and refresh (to renew
 * access tokens) which depend on successful join operation execution.
 *
 * @param props.connection
 * @param props.body Request payload for creating new employee user.
 * @setHeader token.access Authorization
 *
 * @path /auth/employee/join
 * @accessor api.functional.auth.employee.join.joinEmployee
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function joinEmployee(
  connection: IConnection,
  props: joinEmployee.Props,
): Promise<joinEmployee.Response> {
  const output: joinEmployee.Response =
    true === connection.simulate
      ? joinEmployee.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...joinEmployee.METADATA,
            path: joinEmployee.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace joinEmployee {
  export type Props = {
    /** Request payload for creating new employee user. */
    body: IJobPerformanceEvalEmployee.ICreate;
  };
  export type Body = IJobPerformanceEvalEmployee.ICreate;
  export type Response = IJobPerformanceEvalEmployee.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/employee/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/employee/join";
  export const random = (): IJobPerformanceEvalEmployee.IAuthorized =>
    typia.random<IJobPerformanceEvalEmployee.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: joinEmployee.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: joinEmployee.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

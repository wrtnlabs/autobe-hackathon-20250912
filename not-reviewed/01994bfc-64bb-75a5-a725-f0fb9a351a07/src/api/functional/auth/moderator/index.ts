import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ICommunityAiModerator } from "../../../structures/ICommunityAiModerator";

/**
 * Register a new moderator user in the community_ai_moderators table.
 *
 * Registration API for moderators creating new moderator accounts in the
 * community AI platform. It processes moderator identity properties such as
 * email, password hash (generated after receiving plain password), and display
 * name for secure account creation adhering to schema constraints.
 *
 * The API ensures that the moderator role email uniqueness is enforced and
 * timestamps for creation and updates are handled automatically. This supports
 * future login and session management.
 *
 * Moderators receive JWT tokens immediately after joining.
 *
 * This operation is part of the moderator authentication workflow,
 * complementing login and token refresh operations.
 *
 * Improper registration attempts (duplicate email, invalid formats) will be
 * rejected early with appropriate error responses.
 *
 * @param props.connection
 * @param props.body Moderator registration information
 * @setHeader token.access Authorization
 *
 * @path /auth/moderator/join
 * @accessor api.functional.auth.moderator.join
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function join(
  connection: IConnection,
  props: join.Props,
): Promise<join.Response> {
  const output: join.Response =
    true === connection.simulate
      ? join.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...join.METADATA,
            path: join.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace join {
  export type Props = {
    /** Moderator registration information */
    body: ICommunityAiModerator.ICreate;
  };
  export type Body = ICommunityAiModerator.ICreate;
  export type Response = ICommunityAiModerator.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/moderator/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/moderator/join";
  export const random = (): ICommunityAiModerator.IAuthorized =>
    typia.random<ICommunityAiModerator.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: join.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: join.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}


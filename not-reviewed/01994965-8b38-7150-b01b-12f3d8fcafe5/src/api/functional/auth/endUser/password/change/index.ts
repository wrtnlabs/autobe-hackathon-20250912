import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITelegramFileDownloaderEndUser } from "../../../../../structures/ITelegramFileDownloaderEndUser";

/**
 * Change password for authenticated endUser securely.
 *
 * Authenticated password change for end users to update existing password
 * securely. Uses telegram_file_downloader_endusers password_hash for secure
 * updates. Supports member-kind user account management and security
 * enhancement.
 *
 * @param props.connection
 * @param props.body Request body for password change operation for endUser
 * @path /auth/endUser/password/change
 * @accessor api.functional.auth.endUser.password.change.changePassword
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function changePassword(
  connection: IConnection,
  props: changePassword.Props,
): Promise<changePassword.Response> {
  return true === connection.simulate
    ? changePassword.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...changePassword.METADATA,
          path: changePassword.path(),
          status: null,
        },
        props.body,
      );
}
export namespace changePassword {
  export type Props = {
    /** Request body for password change operation for endUser */
    body: ITelegramFileDownloaderEndUser.IChangePassword;
  };
  export type Body = ITelegramFileDownloaderEndUser.IChangePassword;
  export type Response = ITelegramFileDownloaderEndUser.IChangePasswordResponse;

  export const METADATA = {
    method: "POST",
    path: "/auth/endUser/password/change",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/endUser/password/change";
  export const random =
    (): ITelegramFileDownloaderEndUser.IChangePasswordResponse =>
      typia.random<ITelegramFileDownloaderEndUser.IChangePasswordResponse>();
  export const simulate = (
    connection: IConnection,
    props: changePassword.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: changePassword.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

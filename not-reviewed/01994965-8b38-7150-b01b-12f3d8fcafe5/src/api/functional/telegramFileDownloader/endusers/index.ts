import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITelegramFileDownloaderEndUser } from "../../../structures/ITelegramFileDownloaderEndUser";

/**
 * Create a new Telegram File Downloader end user account with email and
 * password hash, plus optional auditing timestamps.
 *
 * Enforces unique email constraint.
 *
 * Returns the created user record with assigned ID.
 *
 * Intended for user registration processes.
 *
 * @param props.connection
 * @param props.body Creation data for Telegram File Downloader end user
 * @path /telegramFileDownloader/endusers
 * @accessor api.functional.telegramFileDownloader.endusers.create
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function create(
  connection: IConnection,
  props: create.Props,
): Promise<create.Response> {
  return true === connection.simulate
    ? create.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...create.METADATA,
          path: create.path(),
          status: null,
        },
        props.body,
      );
}
export namespace create {
  export type Props = {
    /** Creation data for Telegram File Downloader end user */
    body: ITelegramFileDownloaderEndUser.ICreate;
  };
  export type Body = ITelegramFileDownloaderEndUser.ICreate;
  export type Response = ITelegramFileDownloaderEndUser;

  export const METADATA = {
    method: "POST",
    path: "/telegramFileDownloader/endusers",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/telegramFileDownloader/endusers";
  export const random = (): ITelegramFileDownloaderEndUser =>
    typia.random<ITelegramFileDownloaderEndUser>();
  export const simulate = (
    connection: IConnection,
    props: create.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: create.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

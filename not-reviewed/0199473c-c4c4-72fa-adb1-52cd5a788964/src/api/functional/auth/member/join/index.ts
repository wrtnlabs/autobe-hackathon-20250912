import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IChatbotMember } from "../../../../structures/IChatbotMember";

/**
 * Create new member user account and issue authorization tokens based on
 * 'chatbot_members' table.
 *
 * Registers a new member user account, enabling access to the chatbot system
 * features for normal user role 'member'. This includes creating a fresh member
 * record in the database. Input includes nickname and sender IDs internally
 * mapped, consistent with the 'chatbot_members' entity fields. Output includes
 * authorized user information and issued tokens for session management.
 *
 * Role is 'member' - regular authenticated user able to send messages, gain
 * points, and play minigames as described in the requirements documents.
 *
 * Usage mandates public access (no prior token required) for registration flow.
 *
 * The operation is essential for enabling member users to participate in the
 * system and punning a JWT-based authentication model.
 *
 * Related operations include login and refresh token to maintain sessions.
 *
 * @param props.connection
 * @param props.body Member registration request containing data to create a new
 *   member account.
 * @setHeader token.access Authorization
 *
 * @path /auth/member/join
 * @accessor api.functional.auth.member.join.joinMember
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function joinMember(
  connection: IConnection,
  props: joinMember.Props,
): Promise<joinMember.Response> {
  const output: joinMember.Response =
    true === connection.simulate
      ? joinMember.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...joinMember.METADATA,
            path: joinMember.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace joinMember {
  export type Props = {
    /**
     * Member registration request containing data to create a new member
     * account.
     */
    body: IChatbotMember.ICreate;
  };
  export type Body = IChatbotMember.ICreate;
  export type Response = IChatbotMember.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/join";
  export const random = (): IChatbotMember.IAuthorized =>
    typia.random<IChatbotMember.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: joinMember.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: joinMember.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

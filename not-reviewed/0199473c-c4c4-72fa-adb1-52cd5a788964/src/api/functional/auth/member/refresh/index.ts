import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IChatbotMember } from "../../../../structures/IChatbotMember";

/**
 * Refresh tokens for member users maintaining session validity.
 *
 * Refresh JWT tokens for an authenticated member to maintain session continuity
 * in the chatbot system.
 *
 * Requires providing a valid refresh token confirming session validity.
 *
 * Returns new authorization tokens and user details.
 *
 * Role is 'member'.
 *
 * Supports continued access without re-login.
 *
 * Essential for maintaining stateless JWT sessions.
 *
 * @param props.connection
 * @param props.body Refresh token request containing valid token to renew
 *   session.
 * @setHeader token.access Authorization
 *
 * @path /auth/member/refresh
 * @accessor api.functional.auth.member.refresh.refreshMember
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshMember(
  connection: IConnection,
  props: refreshMember.Props,
): Promise<refreshMember.Response> {
  const output: refreshMember.Response =
    true === connection.simulate
      ? refreshMember.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshMember.METADATA,
            path: refreshMember.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshMember {
  export type Props = {
    /** Refresh token request containing valid token to renew session. */
    body: IChatbotMember.IRefresh;
  };
  export type Body = IChatbotMember.IRefresh;
  export type Response = IChatbotMember.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/refresh";
  export const random = (): IChatbotMember.IAuthorized =>
    typia.random<IChatbotMember.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshMember.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshMember.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IChatbotMember } from "../../../../structures/IChatbotMember";

/**
 * Authenticate member user and issue access tokens based on 'chatbot_members'
 * table.
 *
 * Authenticate existing member user credentials and issue JWT access tokens for
 * session management within the chatbot system. Validates credentials against
 * stored user data in the 'chatbot_members' table.
 *
 * Role is 'member'.
 *
 * Publicly accessible for login use case.
 *
 * Returns authenticated user information with issued tokens.
 *
 * Complements join and refresh token flows.
 *
 * @param props.connection
 * @param props.body Member login request with user credentials to authenticate
 *   and obtain tokens.
 * @setHeader token.access Authorization
 *
 * @path /auth/member/login
 * @accessor api.functional.auth.member.login.loginMember
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function loginMember(
  connection: IConnection,
  props: loginMember.Props,
): Promise<loginMember.Response> {
  const output: loginMember.Response =
    true === connection.simulate
      ? loginMember.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...loginMember.METADATA,
            path: loginMember.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace loginMember {
  export type Props = {
    /**
     * Member login request with user credentials to authenticate and obtain
     * tokens.
     */
    body: IChatbotMember.ILogin;
  };
  export type Body = IChatbotMember.ILogin;
  export type Response = IChatbotMember.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/login",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/login";
  export const random = (): IChatbotMember.IAuthorized =>
    typia.random<IChatbotMember.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: loginMember.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: loginMember.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

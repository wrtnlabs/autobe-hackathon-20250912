import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia, { tags } from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

/**
 * Delete chatbot point cooldown by id.
 *
 * Permanently erase a chatbot point cooldown record identified by UUID from the
 * system. This hard delete operation removes the cooldown tracking record
 * completely from the storage.
 *
 * This is an administrative operation with potential impact on cooldown
 * enforcement, so proper authorization is required.
 *
 * Errors such as providing non-existent UUIDs return not-found responses.
 *
 * The operation corresponds to the chatbot_point_cooldowns table in the Prisma
 * schema without any soft deletion.
 *
 * No request body or response body is used.
 *
 * @param props.connection
 * @param props.id Unique identifier of the chatbot point cooldown entry to
 *   delete
 * @path /chatbot/admin/chatbotPointCooldowns/:id
 * @accessor api.functional.chatbot.admin.chatbotPointCooldowns.erase
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function erase(
  connection: IConnection,
  props: erase.Props,
): Promise<void> {
  return true === connection.simulate
    ? erase.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...erase.METADATA,
          path: erase.path(props),
          status: null,
        },
      );
}
export namespace erase {
  export type Props = {
    /** Unique identifier of the chatbot point cooldown entry to delete */
    id: string & tags.Format<"uuid">;
  };

  export const METADATA = {
    method: "DELETE",
    path: "/chatbot/admin/chatbotPointCooldowns/:id",
    request: null,
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = (props: Props) =>
    `/chatbot/admin/chatbotPointCooldowns/${encodeURIComponent(props.id ?? "null")}`;
  export const random = (): void => typia.random<void>();
  export const simulate = (
    connection: IConnection,
    props: erase.Props,
  ): void => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: erase.path(props),
      contentType: "application/json",
    });
    try {
      assert.param("id")(() => typia.assert(props.id));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IChatbotPoints } from "../../../../structures/IChatbotPoints";
import { IPageIChatbotPoints } from "../../../../structures/IPageIChatbotPoints";

/**
 * Search and retrieve chatbot points with filters.
 *
 * Search and retrieve paginated chatbot points for members with flexible
 * filtering and pagination. Useful for leaderboards and administrative
 * monitoring.
 *
 * Request body includes filtering criteria such as member IDs and points range.
 *
 * Response provides paginated list of points with member contextual data.
 *
 * Access restricted to admins to protect privacy.
 *
 * Supports sorting and paging for scalable data retrieval.
 *
 * @param props.connection
 * @param props.body Filtering and pagination options for chatbot points list
 * @path /chatbot/admin/chatbotPoints
 * @accessor api.functional.chatbot.admin.chatbotPoints.index
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function index(
  connection: IConnection,
  props: index.Props,
): Promise<index.Response> {
  return true === connection.simulate
    ? index.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...index.METADATA,
          path: index.path(),
          status: null,
        },
        props.body,
      );
}
export namespace index {
  export type Props = {
    /** Filtering and pagination options for chatbot points list */
    body: IChatbotPoints.IRequest;
  };
  export type Body = IChatbotPoints.IRequest;
  export type Response = IPageIChatbotPoints;

  export const METADATA = {
    method: "PATCH",
    path: "/chatbot/admin/chatbotPoints",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/chatbot/admin/chatbotPoints";
  export const random = (): IPageIChatbotPoints =>
    typia.random<IPageIChatbotPoints>();
  export const simulate = (
    connection: IConnection,
    props: index.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: index.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia, { tags } from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

/**
 * Delete a crawl source by ID.
 *
 * Deletes a crawl source identified by its UUID from the system's database.
 * This will permanently remove the crawl source and all of its associations
 * including crawl schedules, crawl jobs, raw data, and related processing
 * linked to this source.
 *
 * Only users with the admin role are authorized to perform this destructive
 * operation.
 *
 * Errors for non-existent or invalid UUIDs are handled with proper HTTP error
 * codes.
 *
 * No request body is required. No response content is returned on successful
 * deletion.
 *
 * @param props.connection
 * @param props.id Unique identifier of the crawl source to delete
 * @path /politicalNewsCrawler/guest/politicalNewsCrawler/crawlSources/:id
 * @accessor api.functional.politicalNewsCrawler.guest.politicalNewsCrawler.crawlSources.erase
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function erase(
  connection: IConnection,
  props: erase.Props,
): Promise<void> {
  return true === connection.simulate
    ? erase.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...erase.METADATA,
          path: erase.path(props),
          status: null,
        },
      );
}
export namespace erase {
  export type Props = {
    /** Unique identifier of the crawl source to delete */
    id: string & tags.Format<"uuid">;
  };

  export const METADATA = {
    method: "DELETE",
    path: "/politicalNewsCrawler/guest/politicalNewsCrawler/crawlSources/:id",
    request: null,
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = (props: Props) =>
    `/politicalNewsCrawler/guest/politicalNewsCrawler/crawlSources/${encodeURIComponent(props.id ?? "null")}`;
  export const random = (): void => typia.random<void>();
  export const simulate = (
    connection: IConnection,
    props: erase.Props,
  ): void => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: erase.path(props),
      contentType: "application/json",
    });
    try {
      assert.param("id")(() => typia.assert(props.id));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

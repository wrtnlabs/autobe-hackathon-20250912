import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITravelRecordAdmin } from "../../../../structures/ITravelRecordAdmin";

/**
 * Refresh JWT tokens for travel_record_admin user.
 *
 * Refresh JWT access tokens for an authenticated administrator user using a
 * valid refresh token.
 *
 * Ensures continued secure access without re-authentication.
 *
 * Used to prolong session longevity.
 *
 * Must be called with valid refresh tokens only.
 *
 * Related to join and login flows.
 *
 * @param props.connection
 * @param props.body Refresh token data for administrator
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/refresh
 * @accessor api.functional.auth.admin.refresh.refreshAdmin
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshAdmin(
  connection: IConnection,
  props: refreshAdmin.Props,
): Promise<refreshAdmin.Response> {
  const output: refreshAdmin.Response =
    true === connection.simulate
      ? refreshAdmin.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshAdmin.METADATA,
            path: refreshAdmin.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshAdmin {
  export type Props = {
    /** Refresh token data for administrator */
    body: ITravelRecordAdmin.IRefresh;
  };
  export type Body = ITravelRecordAdmin.IRefresh;
  export type Response = ITravelRecordAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/refresh";
  export const random = (): ITravelRecordAdmin.IAuthorized =>
    typia.random<ITravelRecordAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshAdmin.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshAdmin.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

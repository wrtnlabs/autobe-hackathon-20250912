import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITravelRecordAdmin } from "../../../../structures/ITravelRecordAdmin";

/**
 * Create new travel_record_admin user account and issue JWT tokens upon
 * registration.
 *
 * Registers a new administrator account in the travel record system, storing
 * credentials securely and issuing JWT tokens for access.
 *
 * Requires unique email and hashed password fields as per the Prisma schema for
 * travel_record_admin.
 *
 * Enables admins to gain authenticated access with system privileges.
 *
 * Security includes password hashing and soft deletion support using the
 * 'deleted_at' field.
 *
 * Complementary APIs: login for administrators and token refresh for session
 * management.
 *
 * @param props.connection
 * @param props.body Administrator account creation details
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/join
 * @accessor api.functional.auth.admin.join.joinAdmin
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function joinAdmin(
  connection: IConnection,
  props: joinAdmin.Props,
): Promise<joinAdmin.Response> {
  const output: joinAdmin.Response =
    true === connection.simulate
      ? joinAdmin.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...joinAdmin.METADATA,
            path: joinAdmin.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace joinAdmin {
  export type Props = {
    /** Administrator account creation details */
    body: ITravelRecordAdmin.ICreate;
  };
  export type Body = ITravelRecordAdmin.ICreate;
  export type Response = ITravelRecordAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/join";
  export const random = (): ITravelRecordAdmin.IAuthorized =>
    typia.random<ITravelRecordAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: joinAdmin.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: joinAdmin.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITravelRecordAdmin } from "../../../../structures/ITravelRecordAdmin";

/**
 * Authenticate travel_record_admin user and provide JWT tokens.
 *
 * Authenticate administrator user credentials and issue JWT access and refresh
 * tokens.
 *
 * Validates email and password against stored hashed password.
 *
 * Supports system administrator privileged access.
 *
 * Related operations: join for registration, refresh for token renewal.
 *
 * @param props.connection
 * @param props.body Administrator login credentials
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/login
 * @accessor api.functional.auth.admin.login.loginAdmin
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function loginAdmin(
  connection: IConnection,
  props: loginAdmin.Props,
): Promise<loginAdmin.Response> {
  const output: loginAdmin.Response =
    true === connection.simulate
      ? loginAdmin.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...loginAdmin.METADATA,
            path: loginAdmin.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace loginAdmin {
  export type Props = {
    /** Administrator login credentials */
    body: ITravelRecordAdmin.ILogin;
  };
  export type Body = ITravelRecordAdmin.ILogin;
  export type Response = ITravelRecordAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/login",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/login";
  export const random = (): ITravelRecordAdmin.IAuthorized =>
    typia.random<ITravelRecordAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: loginAdmin.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: loginAdmin.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

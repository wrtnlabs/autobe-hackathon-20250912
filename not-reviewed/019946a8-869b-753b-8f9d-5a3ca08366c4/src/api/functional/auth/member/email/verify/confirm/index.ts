/**
 * Confirm email verification for member user.
 *
 * Confirm email verification for TravelRecord member user by providing the
 * verification token. Updates member's email verification status.
 *
 * Ensures valid token usage and prevents spoofing.
 *
 * Related operations: sendEmailVerification for token dispatch.
 *
 * @param props.connection
 * @param props.body Email verification confirmation token.
 * @path /auth/member/email/verify/confirm
 * @accessor api.functional.auth.member.email.verify.confirm.verifyEmailConfirm
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function verifyEmailConfirm(
  connection: IConnection,
  props: verifyEmailConfirm.Props,
): Promise<verifyEmailConfirm.Response> {
  return true === connection.simulate
    ? verifyEmailConfirm.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...verifyEmailConfirm.METADATA,
          path: verifyEmailConfirm.path(),
          status: null,
        },
        props.body,
      );
}
export namespace verifyEmailConfirm {
  export type Props = {
    /** Email verification confirmation token. */
    body: ITravelRecordMember.IVerifyConfirm;
  };
  export type Body = ITravelRecordMember.IVerifyConfirm;
  export type Response = ITravelRecordMember.IResult;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/email/verify/confirm",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/email/verify/confirm";
  export const random = (): ITravelRecordMember.IResult =>
    typia.random<ITravelRecordMember.IResult>();
  export const simulate = (
    connection: IConnection,
    props: verifyEmailConfirm.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: verifyEmailConfirm.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

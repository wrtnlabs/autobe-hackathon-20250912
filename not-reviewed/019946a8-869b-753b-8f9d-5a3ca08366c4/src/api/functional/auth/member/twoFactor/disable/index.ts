import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { ITravelRecordMember } from "../../../../../structures/ITravelRecordMember";

/**
 * Disable two-factor authentication for member user.
 *
 * Disable two-factor authentication (2FA) for TravelRecord member user.
 * Requires valid 2FA token to revoke 2FA status.
 *
 * Ensures account can be secured while allowing revocation.
 *
 * Related operations: enableTwoFactor to activate 2FA.
 *
 * @param props.connection
 * @param props.body Information required to disable 2FA including valid token.
 * @path /auth/member/twoFactor/disable
 * @accessor api.functional.auth.member.twoFactor.disable.disableTwoFactor
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function disableTwoFactor(
  connection: IConnection,
  props: disableTwoFactor.Props,
): Promise<disableTwoFactor.Response> {
  return true === connection.simulate
    ? disableTwoFactor.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...disableTwoFactor.METADATA,
          path: disableTwoFactor.path(),
          status: null,
        },
        props.body,
      );
}
export namespace disableTwoFactor {
  export type Props = {
    /** Information required to disable 2FA including valid token. */
    body: ITravelRecordMember.IDisableTwoFactor;
  };
  export type Body = ITravelRecordMember.IDisableTwoFactor;
  export type Response = ITravelRecordMember.IResult;

  export const METADATA = {
    method: "POST",
    path: "/auth/member/twoFactor/disable",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/member/twoFactor/disable";
  export const random = (): ITravelRecordMember.IResult =>
    typia.random<ITravelRecordMember.IResult>();
  export const simulate = (
    connection: IConnection,
    props: disableTwoFactor.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: disableTwoFactor.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IOauthServerMember } from "../../../structures/IOauthServerMember";

/**
 * Create a new OAuth member account.
 *
 * Create a new OAuth server member account.
 *
 * This operation accepts member creation data including email, password hash,
 * and optionally email verification status.
 *
 * Upon success, a new member record is created with audit timestamps set.
 *
 * This endpoint is publicly accessible to allow user registration.
 *
 * Validation ensures unique email constraint. Passwords must be hashed before
 * submission.
 *
 * Refer to GET and PUT members endpoints for retrieval and update.
 *
 * @param props.connection
 * @param props.body New member creation data
 * @path /oauthServer/oauthServerMembers
 * @accessor api.functional.oauthServer.oauthServerMembers.create
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function create(
  connection: IConnection,
  props: create.Props,
): Promise<create.Response> {
  return true === connection.simulate
    ? create.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...create.METADATA,
          path: create.path(),
          status: null,
        },
        props.body,
      );
}
export namespace create {
  export type Props = {
    /** New member creation data */
    body: IOauthServerMember.ICreate;
  };
  export type Body = IOauthServerMember.ICreate;
  export type Response = IOauthServerMember;

  export const METADATA = {
    method: "POST",
    path: "/oauthServer/oauthServerMembers",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/oauthServer/oauthServerMembers";
  export const random = (): IOauthServerMember =>
    typia.random<IOauthServerMember>();
  export const simulate = (
    connection: IConnection,
    props: create.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: create.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}

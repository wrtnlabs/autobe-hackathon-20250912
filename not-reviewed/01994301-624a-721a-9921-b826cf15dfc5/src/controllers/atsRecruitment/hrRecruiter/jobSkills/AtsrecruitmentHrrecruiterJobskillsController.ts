import { TypedRoute, TypedParam, TypedBody } from "@nestia/core";
import typia, { tags } from "typia";
import { getatsRecruitmentHrRecruiterJobSkillsJobSkillId } from "../../../../providers/getatsRecruitmentHrRecruiterJobSkillsJobSkillId";
import { HrrecruiterAuth } from "../../../../decorators/HrrecruiterAuth";
import { HrrecruiterPayload } from "../../../../decorators/payload/HrrecruiterPayload";
import { putatsRecruitmentHrRecruiterJobSkillsJobSkillId } from "../../../../providers/putatsRecruitmentHrRecruiterJobSkillsJobSkillId";
import { postatsRecruitmentHrRecruiterJobSkills } from "../../../../providers/postatsRecruitmentHrRecruiterJobSkills";

  public async create(
    @HrrecruiterAuth()
    hrRecruiter: HrrecruiterPayload,
    @TypedBody()
    body: IAtsRecruitmentJobSkill.ICreate,
  ): Promise<IAtsRecruitmentJobSkill> {
    try {
      return await postatsRecruitmentHrRecruiterJobSkills({
        hrRecruiter,
        body,
      });
    } catch (error) {
      console.log(error);
      throw error;
    }
  }


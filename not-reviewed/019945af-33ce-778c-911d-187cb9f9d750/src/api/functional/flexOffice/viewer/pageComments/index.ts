import { IFlexOfficePageComment } from "../../../../structures/IFlexOfficePageComment";
import { IPageIFlexOfficePageComment } from "../../../../structures/IPageIFlexOfficePageComment";

/**
 * Search and retrieve a paginated list of FlexOffice page comments.
 *
 * Retrieve a filtered, paginated list of page comments from the FlexOffice
 * system. This operation accesses the flex_office_page_comments table and
 * supports filtering by page, editor, content keyword, and creation date.
 *
 * Security and authorization ensure that only authorized users with permission
 * to view comments can retrieve this list.
 *
 * The response provides a summary of page comments optimized for list views,
 * including comment content previews and metadata.
 *
 * This operation integrates with other endpoints for detailed comment retrieval
 * and comment management.
 *
 * @param props.connection
 * @param props.body Search criteria and pagination parameters for filtering
 *   page comments
 * @path /flexOffice/viewer/pageComments
 * @accessor api.functional.flexOffice.viewer.pageComments.index
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function index(
  connection: IConnection,
  props: index.Props,
): Promise<index.Response> {
  return true === connection.simulate
    ? index.simulate(connection, props)
    : await PlainFetcher.fetch(
        {
          ...connection,
          headers: {
            ...connection.headers,
            "Content-Type": "application/json",
          },
        },
        {
          ...index.METADATA,
          path: index.path(),
          status: null,
        },
        props.body,
      );
}
export namespace index {
  export type Props = {
    /** Search criteria and pagination parameters for filtering page comments */
    body: IFlexOfficePageComment.IRequest;
  };
  export type Body = IFlexOfficePageComment.IRequest;
  export type Response = IPageIFlexOfficePageComment.ISummary;

  export const METADATA = {
    method: "PATCH",
    path: "/flexOffice/viewer/pageComments",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/flexOffice/viewer/pageComments";
  export const random = (): IPageIFlexOfficePageComment.ISummary =>
    typia.random<IPageIFlexOfficePageComment.ISummary>();
  export const simulate = (
    connection: IConnection,
    props: index.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: index.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}


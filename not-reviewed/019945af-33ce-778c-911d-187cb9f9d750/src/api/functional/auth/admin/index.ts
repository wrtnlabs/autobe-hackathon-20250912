import { IFlexOfficeAdmin } from "../../../structures/IFlexOfficeAdmin";

/**
 * Create a new admin account in the flex_office_admins table.
 *
 * Register a new FlexOffice admin by creating an account with unique email and
 * password. The system stores password hashes and issues JWT tokens upon
 * successful join for secure session management.
 *
 * References the `flex_office_admins` table, leveraging fields: `email`,
 * `password_hash`, `created_at`, and `updated_at`.
 *
 * This operation is open to public access for initial registration but will
 * require approval and role assignment for proper admin provisioning.
 *
 * Ensure secure handling of credentials and validation of unique emails to
 * prevent duplicates.
 *
 * Related operations include login to authenticate and refresh to renew tokens.
 *
 * @param props.connection
 * @param props.body Details for creating a new admin account.
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/join
 * @accessor api.functional.auth.admin.join
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function join(
  connection: IConnection,
  props: join.Props,
): Promise<join.Response> {
  const output: join.Response =
    true === connection.simulate
      ? join.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...join.METADATA,
            path: join.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace join {
  export type Props = {
    /** Details for creating a new admin account. */
    body: IFlexOfficeAdmin.ICreate;
  };
  export type Body = IFlexOfficeAdmin.ICreate;
  export type Response = IFlexOfficeAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/join";
  export const random = (): IFlexOfficeAdmin.IAuthorized =>
    typia.random<IFlexOfficeAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: join.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: join.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}


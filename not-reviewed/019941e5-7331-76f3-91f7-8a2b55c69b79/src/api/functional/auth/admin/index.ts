import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEasySignAdmin } from "../../../structures/IEasySignAdmin";

/**
 * Create new administrator account and issue JWT tokens, managing registration
 * in the easy_sign_admins table.
 *
 * This operation allows creation of new administrator accounts with full system
 * privileges. It inserts a new record into the easy_sign_admins table including
 * unique email and username. The operation returns JWT access and refresh
 * tokens encapsulated in IEasySignAdmin.IAuthorized which are used to access
 * protected admin endpoints. This aligns with the business requirements for
 * secure and managed administrator onboarding. Admin users are expected to
 * comply with 2FA authentication procedures.
 *
 * @param props.connection
 * @param props.body Payload for new administrator registration including unique
 *   email and username.
 * @setHeader token.access Authorization
 *
 * @path /auth/admin/join
 * @accessor api.functional.auth.admin.join
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function join(
  connection: IConnection,
  props: join.Props,
): Promise<join.Response> {
  const output: join.Response =
    true === connection.simulate
      ? join.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...join.METADATA,
            path: join.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace join {
  export type Props = {
    /**
     * Payload for new administrator registration including unique email and
     * username.
     */
    body: IEasySignAdmin.ICreate;
  };
  export type Body = IEasySignAdmin.ICreate;
  export type Response = IEasySignAdmin.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/admin/join",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/admin/join";
  export const random = (): IEasySignAdmin.IAuthorized =>
    typia.random<IEasySignAdmin.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: join.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: join.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}


import { Controller } from "@nestjs/common";
import { TypedRoute, TypedBody } from "@nestia/core";
import typia from "typia";
import { patchrecipeSharingGroceryStores } from "../../../providers/patchrecipeSharingGroceryStores";

import { IPageIRecipeSharingGroceryStore } from "../../../api/structures/IPageIRecipeSharingGroceryStore";
import { IRecipeSharingGroceryStore } from "../../../api/structures/IRecipeSharingGroceryStore";

@Controller("/recipeSharing/groceryStores")
export class RecipesharingGrocerystoresController {
  /**
   * Search and browse grocery stores.
   *
   * Retrieve a paginated list of grocery stores with details such as name,
   * address, phone, and website.
   *
   * The list supports filtering and sorting to help users find preferred
   * grocery chains for ingredient price tracking and availability.
   *
   * This is a public endpoint accessible without authentication.
   *
   * @param connection
   * @param body Search criteria and pagination parameters for grocery stores
   * @nestia Generated by Nestia - https://github.com/samchon/nestia
   */
  @TypedRoute.Patch()
  public async index(
    @TypedBody()
    body: IRecipeSharingGroceryStore.IRequest,
  ): Promise<IPageIRecipeSharingGroceryStore.ISummary> {
    try {
      return await patchrecipeSharingGroceryStores({
        body,
      });
    } catch (error) {
      console.log(error);
      throw error;
    }
  }
}

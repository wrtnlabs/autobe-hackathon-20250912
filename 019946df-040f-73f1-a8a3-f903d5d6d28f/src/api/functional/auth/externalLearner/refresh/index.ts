import { IConnection, HttpError } from "@nestia/fetcher";
import { PlainFetcher } from "@nestia/fetcher/lib/PlainFetcher";
import typia from "typia";
import { NestiaSimulator } from "@nestia/fetcher/lib/NestiaSimulator";

import { IEnterpriseLmsExternalLearner } from "../../../../structures/IEnterpriseLmsExternalLearner";

/**
 * Refresh auth tokens for external learner guest user.
 *
 * Refresh tokens for an existing external learner session with a valid refresh
 * token to maintain seamless guest access.
 *
 * This operation checks the validity of the refresh token, the user's current
 * status, and role constraints before issuing new JWT access and refresh
 * tokens.
 *
 * It aligns with the externalLearner schema for security compliance and ensures
 * tokens reflect guest level access.
 *
 * Session management includes token expiry and revocation policies.
 *
 * It returns tokens compliant with EnterpriseLms external learner role
 * authorization scope.
 *
 * This operation is intended for authorized users only and will reject invalid
 * or expired tokens.
 *
 * @param props.connection
 * @param props.body Refresh token request payload with a valid refresh token
 * @setHeader token.access Authorization
 *
 * @path /auth/externalLearner/refresh
 * @accessor api.functional.auth.externalLearner.refresh.refreshExternalLearner
 * @autobe Generated by AutoBE - https://github.com/wrtnlabs/autobe
 */
export async function refreshExternalLearner(
  connection: IConnection,
  props: refreshExternalLearner.Props,
): Promise<refreshExternalLearner.Response> {
  const output: refreshExternalLearner.Response =
    true === connection.simulate
      ? refreshExternalLearner.simulate(connection, props)
      : await PlainFetcher.fetch(
          {
            ...connection,
            headers: {
              ...connection.headers,
              "Content-Type": "application/json",
            },
          },
          {
            ...refreshExternalLearner.METADATA,
            path: refreshExternalLearner.path(),
            status: null,
          },
          props.body,
        );
  connection.headers ??= {};
  connection.headers.Authorization = output.token.access;
  return output;
}
export namespace refreshExternalLearner {
  export type Props = {
    /** Refresh token request payload with a valid refresh token */
    body: IEnterpriseLmsExternalLearner.IRefresh;
  };
  export type Body = IEnterpriseLmsExternalLearner.IRefresh;
  export type Response = IEnterpriseLmsExternalLearner.IAuthorized;

  export const METADATA = {
    method: "POST",
    path: "/auth/externalLearner/refresh",
    request: {
      type: "application/json",
      encrypted: false,
    },
    response: {
      type: "application/json",
      encrypted: false,
    },
  } as const;

  export const path = () => "/auth/externalLearner/refresh";
  export const random = (): IEnterpriseLmsExternalLearner.IAuthorized =>
    typia.random<IEnterpriseLmsExternalLearner.IAuthorized>();
  export const simulate = (
    connection: IConnection,
    props: refreshExternalLearner.Props,
  ): Response => {
    const assert = NestiaSimulator.assert({
      method: METADATA.method,
      host: connection.host,
      path: refreshExternalLearner.path(),
      contentType: "application/json",
    });
    try {
      assert.body(() => typia.assert(props.body));
    } catch (exp) {
      if (!typia.is<HttpError>(exp)) throw exp;
      return {
        success: false,
        status: exp.status,
        headers: exp.headers,
        data: exp.toJSON().message,
      } as any;
    }
    return random();
  };
}
